# Son dos valores iguales? 
# Para hacer esta pregunta puedes hacer uso del operador ==
# No olvidar que = es un operador de asignacion mientras que == es una pregunta de comparacion

# Ejemplos 
# 2 == 2 True
# 2 == 2. True
# 1 == 2 False 
# var == 0

# black_sheep == 2 * white_sheep

# black_sheep == (2 * white_sheep)

var = 0  # Assigning 0 to var
print(var == 0)

var = 1  # Assigning 1 to var
print(var == 0)

# != Este operador es de desigualdad, al igual que el anterior compara los valores pero solo si son diferentes es verdadero
var = 0  # Assigning 0 to var
print(var != 0)
 
var = 1  # Assigning 1 to var
print(var != 0)
 
